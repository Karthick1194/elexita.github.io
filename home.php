    <q>"To produce young engineers with strong technical exposure in the areas of Electronics and Communication Engineering by providing world class education."</q>
    <p>We are living in the Electronics Age, technologically speaking. Every application or device we have or use tends to become electronic in technology. The 'e-' prefix sprouting on almost every conceivable application – e-mail, e-ticket, e-commerce, e-court, e-cash, etc. – bears compelling evidence to the maturing of Electronics Age. Though most of these applications are the offshoots of Information Technology, the underlying foundation on which they are built is 'Electronics', in terms of hardware.The infrastructure that enabled such new applications is an allied technology called 'Communication Engineering'. While the field of Communication Engineering has grown so huge to become a discipline by itself the discipline of Electronics Engineering can never be complete without the 'Communication Engineering' component.The Bachelor programme of study in this department teaches the students the science and application of electronics principles, electronic circuit design, microprocessors, television engineering, and networking. The engineers of this discipline will be able to design devices and circuits for newer applications in industries or home. They will also be able to design networks for digital and radio communications. With their knowledge in electronics and skill in instruments like oscilloscopes and digital probes they will be able to troubleshoot any electronic devices and electronic component-based equipments also. Further specialization in Communication Systems is offered by the department in its Master degree programme in Communication Systems.The Department of Electronics and Communication Engineering in Sudharsan Engineering College enjoys a generous investment in equipment which is well-utilized in intensive laboratory training for students. Initiatives in innovative circuit designs and applications are encouraged offering free access to laboratory resources. The Laboratory stock is continually updated and upgraded in keeping with the advancements in